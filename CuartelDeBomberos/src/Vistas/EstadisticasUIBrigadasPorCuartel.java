    //SELECCIONO EL CUARTEL
    private void jCboxCuartelesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jCboxCuartelesActionPerformed

        borrarFilas(); //CADA VEZ QUE CARGO BORRO LA TABLA
        BrigadaData brigadaData = new BrigadaData();
        ArrayList<Brigada> listaDeBrigadaPorCuartel=new ArrayList();
        String dato = jCboxCuarteles.getSelectedItem().toString();
        String codigo = "";
        String estado="";
        if (!dato.isEmpty()) {
            for (int i = 0; i < dato.length(); i++) {
                if (dato.charAt(i) != '-') {
                    codigo = codigo + dato.charAt(i);
                } else {
                    break;
                }
            }
            listaDeBrigadaPorCuartel=brigadaData.listarBrigadasPorCuartel(Integer.parseInt(codigo));
            for (Brigada brigada : listaDeBrigadaPorCuartel) {
                if (brigada.isEstado()){
                    estado="Activa";
                }else {
                    estado="Inactivo";
                }
                tablaModelo.addRow(new Object[]{ brigada.getCodigoBrigada(), brigada.getNombreBrigada(),brigada.getEspecialidad().getEmergencia(),estado});
            }
        }

    }//GEN-LAST:event_jCboxCuartelesActionPerformed


