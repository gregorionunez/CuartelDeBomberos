    //CARGO LOS CUARTELES EN EL COMBOBOX
    private void cargarCuarteles() {
        ArrayList<Cuartel> listaCuartel = new ArrayList();
        CuartelData cuartelData = new CuartelData();
        listaCuartel = cuartelData.listarCuarteles();
        for (Cuartel cuartel : listaCuartel) {
            jCboxCuarteles.addItem(cuartel.getCodCuartel() + "-" + cuartel.getNombreCuartel());
        }
    }

    //LIMPIO LAS FILAS
    private void borrarFilas() {

        int filas = jTablaListado.getRowCount() - 1;
        for (int f = filas; f >= 0; f--) {
            tablaModelo.removeRow(f);

        }
    }

